
def compare_colors(results):
    if results == 'Black':
        return [0, 0, 0]
    elif results == 'Pink':
        return [255, 51, 153]
    elif results == 'Red':
        return [255, 0, 0]
    elif results == 'Orange':
        return [255, 128, 0]
    elif results == 'Green':
        return [0, 255, 0]
    elif results == 'Blue':
        return [0, 0, 255]
    elif results == 'Brown':
        return [255, 128, 0]
    elif results == 'Yellow':
        return [255, 255, 0]
    elif results == 'Purple':
        return [153, 51, 255]
    elif results == 'White':
        return [255, 255, 255]
    elif results == 'Grey':
        return [192, 192, 192]


